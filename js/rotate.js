$(document).ready(function () {
  let rotate = 0;
  $('#summaryKits').click(()=>{
      rotate += 180;
      $('#caret-down-kits').css('transform',`rotate(${rotate}deg)`)
      if(rotate === 180){
        rotate =- 180;
      }
  })
});
